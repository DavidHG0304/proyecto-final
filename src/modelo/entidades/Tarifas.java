package modelo.entidades;

public class Tarifas {
	private int idTarifas;
	private Float seguro_danios;
	private Float seguro_vida;
	private Float seguro_kilometraje;
	private Float seguro_combustible;
	private Float seguro_tarifa_por_dia;
	
	public Tarifas() {
		
	}

	public int getIdTarifas() {
		return idTarifas;
	}

	public void setIdTarifas(int idTarifas) {
		this.idTarifas = idTarifas;
	}

	public Float getSeguro_danios() {
		return seguro_danios;
	}

	public void setSeguro_danios(Float seguro_danios) {
		this.seguro_danios = seguro_danios;
	}

	public Float getSeguro_vida() {
		return seguro_vida;
	}

	public void setSeguro_vida(Float seguro_vida) {
		this.seguro_vida = seguro_vida;
	}

	public Float getSeguro_kilometraje() {
		return seguro_kilometraje;
	}

	public void setSeguro_kilometraje(Float seguro_kilometraje) {
		this.seguro_kilometraje = seguro_kilometraje;
	}

	public Float getSeguro_combustible() {
		return seguro_combustible;
	}

	public void setSeguro_combustible(Float seguro_combustible) {
		this.seguro_combustible = seguro_combustible;
	}

	public Float getSeguro_tarifa_por_dia() {
		return seguro_tarifa_por_dia;
	}

	public void setSeguro_tarifa_por_dia(Float seguro_tarifa_por_dia) {
		this.seguro_tarifa_por_dia = seguro_tarifa_por_dia;
	}
	
	
	
}
